function translateStaticDashboardText(language) {
    if (typeof document === 'undefined') return;
    const map = language === 'ar' ? DASHBOARD_TEXT_AR : DASHBOARD_TEXT_FR;
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
    const nodes = [];

    while (walker.nextNode()) {
        nodes.push(walker.currentNode);
    }

    nodes.forEach((node) => {
        const text = node.nodeValue;
        const trimmed = text.trim();
        if (!trimmed || !map[trimmed]) return;
        node.nodeValue = text.replace(trimmed, map[trimmed]);
    });
}

const LanguageContext = createContext(null);

export function LanguageProvider({ children }) {
    const [language, setLanguage] = useState(() => localStorage.getItem('pme-language') || 'fr');
    const direction = language === 'ar' ? 'rtl' : 'ltr';

    useEffect(() => {
        localStorage.setItem('pme-language', language);
        document.documentElement.lang = language;
        document.documentElement.dir = direction;
        document.body.dir = direction;

        const applyTranslations = () => translateStaticDashboardText(language);
        const timer = window.setTimeout(applyTranslations, 0);
        const observer = new MutationObserver(applyTranslations);
        observer.observe(document.body, { childList: true, subtree: true });

        return () => {
            window.clearTimeout(timer);
            observer.disconnect();
        };
    }, [language, direction]);

    const value = useMemo(() => ({
        language,
        direction,
        setLanguage,
        t: (key) => STRINGS[language]?.[key] || STRINGS.fr[key] || key,
    }), [language, direction]);

    return (
        <LanguageContext.Provider value={value}>
            {children}
        </LanguageContext.Provider>
    );
}

export function useLanguage() {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error('useLanguage must be used inside LanguageProvider');
    }
    return context;
}
